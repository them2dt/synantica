/**
 * Get flag emoji for a country
 */
export function getCountryFlag(country: string): string {
  const normalizedCountry = country.toLowerCase().replace(/\s+/g, '-')
  return COUNTRY_FLAGS[normalizedCountry] || '馃實'
}

